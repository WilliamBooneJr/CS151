import stdio

#Create house class

class House:
    def __init__(self, address, price, sqft, beds, baths, garage=True):
        self._address = address
        self._price = price
        self._sqft = sqft
        self._beds = beds
        self._baths = baths
        self._garage = garage


def get_address(self):
    return self._address

def get_price(self):
    return self._price

def get_sqft(self):
    return self._sqft

def get_beds(self):
    return self._beds

def get_baths(self):
    return self._baths

def get_garage(self):
    return self._garage 

def set_price(self, price):
    self._price = price


